def url_escape(bstring, pattern=None):
    if pattern is not None:
        pattern = b'([' + pattern + b'])'
    else:
        pattern = br'([^A-Za-z0-9\-._~])'

    replacement = lambda m: b'%' + format(ord(m[1]), 'X').encode('ascii')

    return bstring == s(pattern, replacement, 'gr')


